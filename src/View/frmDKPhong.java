    public void HienThiDSPhong() {
        Vector<clssLoaiPhong> ds = new Vector<clssLoaiPhong>();

        int ketqua = CSDL.tbPhong.DSPhong(ds);
        if (ketqua == -1) {
            JOptionPane.showMessageDialog(this, "Loi ket noi sql");
            return;
        } else if (ketqua == -2) {
            JOptionPane.showMessageDialog(this, "Loi sql");
            return;
        } else {
            DefaultTableModel dtm = (DefaultTableModel) this.tblGia.getModel();
            dtm.setRowCount(0);
            for (clssLoaiPhong dss : ds)
                dtm.addRow(new Object[] { dss.getLoaiPhong(), dss.getDonGia() });
        }
    }

    public void HienThiDSHoaDon() {
        Connection conn = CSDL.Database.ConnectDatabase();
        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Loi ket noi");
            return;
        }
        String soPhong = this.txtSoPhong.getText();
        String sql = "select * from phong_" + soPhong;

        try {
            PreparedStatement stm = conn.prepareStatement(sql);
            ResultSet rs = stm.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel) this.tblHoaDon.getModel();
            dtm.setNumRows(0);
            while (rs.next()) {

                int setID = rs.getInt("ID");
                String loaiPhong = rs.getString("loaiPhong");
                float donGia = rs.getFloat("donGia");
                int soGio = rs.getInt("soGio");
                String hoaDon = rs.getString("hoaDon");
                String ngayNhanPhong = rs.getString("ngayNhan");
                String ngayTraPhong = rs.getString("ngayTra");
                int maHoaDon = rs.getInt("maHoaDon");
                String tenKhachHang = rs.getString("tenKhachHang");
                String ngayLapHoaDon = rs.getString("ngayLapHoaDon");
                float thanhTien = rs.getFloat("thanhTien");
                int soNgayO = rs.getInt("soNgayO");

                dtm.addRow(new Object[] { setID, loaiPhong, Float.toString(donGia), Integer.toString(soGio), hoaDon,
                        ngayNhanPhong, ngayTraPhong, Integer.toString(maHoaDon), tenKhachHang, ngayLapHoaDon,
                        Float.toString(thanhTien), soNgayO });

                this.txtLoaiPhong.setText("");
                this.txtDonGiaPhong.setText("");
                this.txtSoGioSD.setText("");
                this.txtLoaiHoaDon.setText("");
                this.txtNgayDen.setText("");
                this.txtNgayTraPhong.setText("");
                this.txtMaHoaDon.setText("");
                this.txtTenKhachHang.setText("");
                this.txtNgayLapHoaDon.setText("");
                this.txtThanhTien.setText("");

            }

        } catch (SQLException ex) {
            Logger.getLogger(frmDKPhong.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Loi sql " + ex.getMessage());
            return;
        }
    }

