    private void btnThanhTienActionPerformed(java.awt.event.ActionEvent evt) {

        String soGio = this.txtSoGioSD.getText();
        String loaiPhong = this.txtLoaiPhong.getText();
        String getNgayNhan = this.txtNgayDen.getText();
        String getNgayTra = this.txtNgayTraPhong.getText();

        DateTimeFormatter dateTimeFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate dateNhan = LocalDate.parse(getNgayNhan, dateTimeFormatter);
        LocalDate dateTra = LocalDate.parse(getNgayTra, dateTimeFormatter);

        int getDateNhanInt = dateNhan.getDayOfMonth();
        int gettDateTraInt = dateTra.getDayOfMonth();

        int soNgayOLai = gettDateTraInt - getDateNhanInt;

        if (loaiPhong.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa chọn phòng ");
            return;
        }

        if (soGio.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập số ngày");
            return;
        } else if (soNgayOLai > 7) {
            this.txtSoGioSD.setText(Integer.toString(soNgayOLai * 24));
        }

        if (ktso(soGio) == false) {
            JOptionPane.showMessageDialog(this, "Số ngày phải là số");
            return;
        }

        float donGia = Float.parseFloat(this.txtDonGiaPhong.getText());
        int soGioSD = Integer.parseInt(soGio);

        if (soGioSD < 30 && soGioSD > 24) {
            soGioSD = 24;
            this.txtThanhTien.setText(Float.toString(donGia * soGioSD));
            this.txtLoaiHoaDon.setText("Hoa Don Gio");
        } else if (soNgayOLai > 7) {
            float ThanhToan = donGia * soNgayOLai * 0.8f;
            this.txtThanhTien.setText(Float.toString(ThanhToan));
            this.txtLoaiHoaDon.setText("Hoa Don Ngay");
        } else if (soGioSD < 24) {
            float ThanhToan = donGia * soGioSD;
            this.txtThanhTien.setText(Float.toString(ThanhToan));
            this.txtLoaiHoaDon.setText("Hoa Don Gio");
        } else {
            this.txtThanhTien.setText(Float.toString(donGia * soNgayOLai));
            this.txtLoaiHoaDon.setText("Hoa Don Ngay");
            this.txtSoGioSD.setText(Integer.toString(soNgayOLai * 24));
            return;
        }
    }

    public static int generateRandomIdHoaDon() {
        Random random = new Random();
        int min = 111111111; // Giá trị nhỏ nhất của khoảng số ngẫu nhiên
        int max = 999999999; // Giá trị lớn nhất của khoảng số ngẫu nhiên
        int randomNumber = random.nextInt(max - min + 1) + min;
        return randomNumber;
    }

    private void addBill(java.awt.event.ActionEvent evt) {
        DefaultTableModel dtm = (DefaultTableModel) this.tblHoaDon.getModel();
        int setID = generateRandomIdHoaDon();
        String loaiPhong = this.txtLoaiPhong.getText();
        String donGia = this.txtDonGiaPhong.getText();
        String soGio = this.txtSoGioSD.getText();
        String ngayNhanPhong = this.txtNgayDen.getText();
        String hoaDon = this.txtLoaiHoaDon.getText();
        String ngayTraPhong = this.txtNgayTraPhong.getText();
        String maHoaDon = this.txtMaHoaDon.getText();
        String tenKhachHang = this.txtTenKhachHang.getText();
        String ngayLapHoaDon = this.txtNgayLapHoaDon.getText();
        String thanhTien = this.txtThanhTien.getText();

        DateTimeFormatter dateTimeFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate dateNhan = LocalDate.parse(ngayNhanPhong, dateTimeFormatter);
        LocalDate dateTra = LocalDate.parse(ngayTraPhong, dateTimeFormatter);

        int dateNhanInt = dateNhan.getDayOfMonth();
        int dateTraInt = dateTra.getDayOfMonth();

        int calculationDate = dateTraInt - dateNhanInt;

        if (thanhTien.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa thành tiền");
            return;
        }

        Connection conn = CSDL.Database.ConnectDatabase();
        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Loi ket noi");
            return;
        }
        String soPhong = this.txtSoPhong.getText();
        String sql = "insert into phong_" + soPhong
                + "(ID, loaiphong, donGia, soGio, hoaDon, ngayNhan, ngayTra, maHoaDon, tenKhachHang, ngayLapHoaDon, thanhTien, soNgayO) "
                + " values(?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

        PreparedStatement stm;
        try {
            stm = conn.prepareStatement(sql);
            stm.setInt(1, setID);
            stm.setString(2, loaiPhong);
            stm.setFloat(3, Float.parseFloat(donGia));
            stm.setInt(4, Integer.parseInt(soGio));
            stm.setString(5, hoaDon);
            stm.setString(6, ngayNhanPhong);
            stm.setString(7, ngayTraPhong);
            stm.setInt(8, Integer.parseInt(maHoaDon));
            stm.setString(9, tenKhachHang);
            stm.setString(10, ngayLapHoaDon);
            stm.setFloat(11, Float.parseFloat(thanhTien));
            stm.setInt(12, calculationDate);

            int n = stm.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmDKPhong.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Loi sql " + ex.getMessage());
            return;
        }

        HienThiDSHoaDon();

        this.txtLoaiPhong.setText("");
        this.txtDonGiaPhong.setText("");
        this.txtSoGioSD.setText("");
        this.txtLoaiHoaDon.setText("");
        this.txtNgayDen.setText("");
        this.txtNgayTraPhong.setText("");
        this.txtMaHoaDon.setText("");
        this.txtTenKhachHang.setText("");
        this.txtNgayLapHoaDon.setText("");
        this.txtThanhTien.setText("");

        HienThiDSPhong();

    }// GEN-LAST:event_btnThemMonHoaDonActionPerformed

